import java.util.*;

public class charat {
    
    public static void main(String args[]){
        String fname="Charul";
        String lname="Tongaria";
        String name=fname + " " + lname;
        for(int i=0; i<name.length(); i++){
            System.out.print(name.charAt(i)+" ");
        }
        System.out.println();
    }

}
